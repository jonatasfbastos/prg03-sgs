    public void readJTable() {

        log.info("üîÑ Atualizando a tabela...");

        // Verifica se o controller foi injetado corretamente
        if (campanhaVacinaController == null) {
            log.error("‚ùå ERRO: cursoController nao foi injetado corretamente!");
            return;
        }

        // Verifica se tem objeto no banco
        List<CampanhaVacinaEntity> campanhas = campanhaVacinaController.findAll();
        if (campanhas == null || campanhas.isEmpty()) {
            log.warn("‚ö† Nenhum curso encontrado.");
            return;
        }

        // Verifica de a tablea est√° nulla
        if (tabelaExibirCampanha == null) {
            log.error("‚ùå ERRO: tabelaExibir est√° nula!");
            return;
        }

        // Obtem o modelo da tabela e verifica se t√° nullo
        DefaultTableModel model = (DefaultTableModel) tabelaExibirCampanha.getModel();
        if (model == null) {
            log.error("‚ùå ERRO: O modelo da tabela est√° nulo!");
            return;
        }

        // Limpa a tabela antes exibir
        model.setRowCount(0); 

        
        // Popula o banco
        // Popula a tabela
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm"); // Formato desejado
        for (CampanhaVacinaEntity c : campanhas) {
            model.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getLocalVacina(),
                c.getInicioCampanha().format(formatter),
                c.getFinalCampanha() != null ? c.getFinalCampanha().format(formatter) : "N/A",// Verifica se finalCampanha √© nulo
                c.isDisponibilidade() ? "Sim" : "N√£o",
                c.getPublicoAlvo(),
            });
        }
        log.info("‚úÖ Tabela atualizada com sucesso!");
    }

